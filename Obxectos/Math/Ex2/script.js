let x = Math.floor(Math.random() * (101 - 1) + 1);
let y = Math.floor(Math.random() * (51 - 1) + 1);
console.log(x + ' + ' + y + ' = ' + (x + y));
console.log(x + ' - ' + y + ' = ' + (x - y));
console.log(x + ' * ' + y + ' = ' + (x * y));
console.log(x + ' / ' + y + ' = ' + (x / y));
console.log(x + ' % ' + y + ' = ' + (x % y));